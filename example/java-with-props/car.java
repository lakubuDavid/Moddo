public class Car{
	private int id;

	public int getId() {
		return id;
	}
	public int setId(int value) {
		 id = value;
	}

	private String owner;

	public String getOwner() {
		return owner;
	}
	public String setOwner(String value) {
		 owner = value;
	}

	private String color;

	public String getColor() {
		return color;
	}
	public String setColor(String value) {
		 color = value;
	}

	private object paint;

	public object getPaint() {
		return paint;
	}
	public object setPaint(object value) {
		 paint = value;
	}

	private float fuel;

	public float getFuel() {
		return fuel;
	}
	public float setFuel(float value) {
		 fuel = value;
	}

}
